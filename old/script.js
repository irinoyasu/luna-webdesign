/* ==========================================================================
   STATE VARIABLES
   ========================================================================== */
let currentView = "home";
let currentPresentationKey = null;
let currentSlideIndex = 0;
let commentsDatabase = {}; // Stores both default and custom comments in memory/localStorage

/* ==========================================================================
   INITIALIZATION
   ========================================================================== */
document.addEventListener("DOMContentLoaded", () => {
  // 1. Splash Screen timeout (Hides splash after 2.5s)
  setTimeout(() => {
    const splash = document.getElementById("splash-screen");
    if (splash) {
      splash.classList.add("fade-out");
      // Fully remove display after transition completed to prevent layout issues
      setTimeout(() => {
        splash.style.display = "none";
      }, 600);
    }
  }, 2200);

  // 2. Initialize Comments Database from LocalStorage or Defaults
  initializeComments();

  // 3. Set Home active initially
  navigateTo("home");
});

/* ==========================================================================
   NAVIGATION ROUTER
   ========================================================================== */
function navigateTo(viewId) {
  currentView = viewId;
  
  // Update view containers visibility
  const views = document.querySelectorAll(".view");
  views.forEach(v => {
    v.classList.remove("active");
  });

  const activeView = document.getElementById(`${viewId}-view`);
  if (activeView) {
    activeView.classList.add("active");
  }

  // Update navbar buttons styling
  const navBtns = document.querySelectorAll(".nav-btn");
  navBtns.forEach(btn => {
    btn.classList.remove("active");
  });

  if (viewId === "home") {
    document.getElementById("btn-home").classList.add("active");
  } else if (viewId === "album") {
    document.getElementById("btn-album").classList.add("active");
  } else if (viewId === "slides") {
    document.getElementById("btn-album").classList.add("active"); // Slide inherits Album nav active state
  }

  // Smooth scroll back to top
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

function scrollToSection(sectionId) {
  // If we're not on the home view, navigate home first
  if (currentView !== "home") {
    navigateTo("home");
    // Delay slightly to allow transition
    setTimeout(() => {
      performScroll(sectionId);
    }, 150);
  } else {
    performScroll(sectionId);
  }
}

function performScroll(sectionId) {
  const section = document.getElementById(sectionId);
  if (section) {
    section.scrollIntoView({ behavior: "smooth", block: "start" });
  }
}

/* ==========================================================================
   PRESENTATION VIEWER LOGIC
   ========================================================================== */
function viewPresentation(key) {
  const pres = presentations[key];
  if (!pres) return;

  currentPresentationKey = key;
  currentSlideIndex = 0;

  // Set presentation title metadata
  document.getElementById("pres-author-badge").innerText = `布教者: ${pres.author}`;
  document.getElementById("pres-date-text").innerText = pres.date;
  document.getElementById("pres-title-text").innerText = pres.title;

  // Set total slide counts
  document.getElementById("total-slides-num").innerText = pres.slides.length;

  // Render Slide and Comments
  renderSlide();
  renderComments();

  // Navigate to Slides View
  navigateTo("slides");
}

function renderSlide() {
  const pres = presentations[currentPresentationKey];
  const slide = pres.slides[currentSlideIndex];
  const slideScreen = document.getElementById("slide-screen");

  // Remove existing color classes
  slideScreen.classList.remove("bg-pink", "bg-yellow", "bg-lavender");
  
  // Set theme color class
  if (pres.color === "pink") {
    slideScreen.classList.add("bg-pink");
  } else if (pres.color === "yellow") {
    slideScreen.classList.add("bg-yellow");
  } else if (pres.color === "lavender") {
    slideScreen.classList.add("bg-lavender");
  }

  // Set Slide Contents
  slideScreen.innerHTML = `
    <div class="slide-content">
      <h3 class="slide-title">${slide.title}</h3>
      <div class="slide-body">${slide.body}</div>
      <div class="slide-illustration">${slide.emoji}</div>
    </div>
  `;

  // Update Slide Indicator
  document.getElementById("current-slide-num").innerText = currentSlideIndex + 1;
}

let tvDialAngle = 0;
function changeSlide(direction) {
  const pres = presentations[currentPresentationKey];
  const nextIndex = currentSlideIndex + direction;

  if (nextIndex >= 0 && nextIndex < pres.slides.length) {
    currentSlideIndex = nextIndex;
    renderSlide();
    
    // Rotate TV Channel dial
    tvDialAngle += direction * 40;
    const dial = document.getElementById("tv-dial-channel");
    if (dial) {
      dial.style.transform = `rotate(${tvDialAngle}deg)`;
    }
  }
}

// Retro TV Power Switch
let tvPowerOn = true;
function toggleTvPower() {
  const led = document.getElementById("tv-power-led");
  const screen = document.getElementById("slide-screen");
  const channelDial = document.getElementById("tv-dial-channel");
  const volumeDial = document.getElementById("tv-dial-volume");
  
  tvPowerOn = !tvPowerOn;
  
  if (tvPowerOn) {
    led.classList.remove("power-off");
    screen.classList.remove("power-off");
    // Play sound or light flicker effect via CSS
    screen.innerHTML = ""; // force re-render active slide
    renderSlide();
  } else {
    led.classList.add("power-off");
    screen.classList.add("power-off");
    screen.innerHTML = `
      <div class="tv-off-noise">
        <div class="crt-static"></div>
      </div>
    `;
    // Rotate volume dial off slightly
    if (volumeDial) {
      volumeDial.style.transform = "rotate(-90deg)";
    }
  }
}

/* ==========================================================================
   FEEDBACK & STICKY NOTE SYSTEM
   ========================================================================== */
function initializeComments() {
  const saved = localStorage.getItem("luna_サークル_comments");
  if (saved) {
    commentsDatabase = JSON.parse(saved);
  } else {
    // Populate defaults from presentation files
    Object.keys(presentations).forEach(key => {
      commentsDatabase[key] = [...presentations[key].defaultComments];
    });
    saveCommentsToStorage();
  }
}

function saveCommentsToStorage() {
  localStorage.setItem("luna_サークル_comments", JSON.stringify(commentsDatabase));
}

function renderComments() {
  const board = document.getElementById("feedback-board");
  board.innerHTML = "";

  const comments = commentsDatabase[currentPresentationKey] || [];
  
  comments.forEach((c, index) => {
    const card = document.createElement("div");
    // Random slight rotation between -3deg and +3deg for scrapbook style
    const randomAngle = (Math.random() * 6 - 3).toFixed(1);
    card.className = `feedback-sticky bg-${c.color}-pale`;
    card.style.transform = `rotate(${randomAngle}deg)`;
    card.setAttribute("onclick", `openFeedbackDetail(${index})`);
    
    // Pick a random tape color for the comment card
    const tapes = ["pink", "yellow", "blue", "lavender", "mint"];
    const randomTape = tapes[Math.floor(Math.random() * tapes.length)];
    
    card.innerHTML = `
      <div class="washi-tape-sticky tape-${randomTape}"></div>
      <p class="sticky-excerpt">「${escapeHTML(c.text)}」</p>
      <div class="sticky-footer">
        <span class="sticky-author">${escapeHTML(c.author)}</span>
      </div>
    `;
    
    board.appendChild(card);
  });
}

function handleCommentSubmit(event) {
  event.preventDefault();

  const authorInput = document.getElementById("comment-author");
  const textInput = document.getElementById("comment-text");
  const colorInput = document.querySelector('input[name="sticky-color"]:checked');

  const author = authorInput.value.trim();
  const text = textInput.value.trim();
  const color = colorInput ? colorInput.value : "yellow";

  if (!author || !text) return;

  // Add to database
  if (!commentsDatabase[currentPresentationKey]) {
    commentsDatabase[currentPresentationKey] = [];
  }

  commentsDatabase[currentPresentationKey].push({
    author: author,
    color: color,
    text: text
  });

  saveCommentsToStorage();
  renderComments();

  // Reset form inputs except author name for convenience
  textInput.value = "";
  
  // Custom mini animation for feedback notification
  const board = document.getElementById("feedback-board");
  const newCard = board.lastElementChild;
  if (newCard) {
    newCard.style.animation = "stickyDrop 0.5s cubic-bezier(0.175, 0.885, 0.32, 1.275) forwards";
  }
}

