/* ==========================================================================
   MODAL INTERACTIVE POPUPS
   ========================================================================== */
function openFeedbackDetail(index) {
  const comments = commentsDatabase[currentPresentationKey] || [];
  const comment = comments[index];
  if (!comment) return;

  const modal = document.getElementById("feedback-modal");
  document.getElementById("modal-author-title").innerText = `${escapeHTML(comment.author)} さんの感想`;
  document.getElementById("modal-feedback-text").innerHTML = `「${escapeHTML(comment.text)}」`;

  modal.classList.add("active");
}

function closeFeedbackModal() {
  const modal = document.getElementById("feedback-modal");
  modal.classList.remove("active");
}

